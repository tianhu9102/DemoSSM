package cn.thu.util;

/**
 * Points<br>
 * 定义三维坐标点
 * 
 * @author
 * @since 2018年12月7日
 */
public class Point
{

    private double x; // x坐标
    private double y; // y坐标
    private double z; // z坐标

    public double getX()
    {
        return x;
    }

    public void setX(double x)
    {
        this.x = x;
    }

    public double getY()
    {
        return y;
    }

    public void setY(double y)
    {
        this.y = y;
    }

    public double getZ()
    {
        return z;
    }

    public void setZ(double z)
    {
        this.z = z;
    }

}